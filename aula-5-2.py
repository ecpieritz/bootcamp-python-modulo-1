#if
a = 8
b = 20
c = 8

#igual
if a == c:
    print('A é igual a C')

#diferente
if a != b:
    print('A é diferente a B')

#menor que
if a < b:
    print('A é menor que B')

#menor ou igual que
if a <= b:
    print('A é menor ou igual que B')

#maior que
if b > c:
    print('B é maior que C')

#maior ou igual que
if b >= c:
    print('B é maior ou igual que C')
